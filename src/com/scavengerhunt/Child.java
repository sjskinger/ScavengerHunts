package com.scavengerhunt;

public class Child {
	private String Name;
	private String Tag;
	
	public Child(String name, String tag) {
		super();
		Name = name;
		Tag = tag;
	}

	public String getName() {
		return Name;
	}

	public void setName(String Name) {
		this.Name = Name;
	}

	public String getTag() {
		return Tag;
	}

	public void setTag(String Tag) {
		this.Tag = Tag;
	}

}

